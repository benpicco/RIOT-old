static void
test_timetlv_decoding(void) {
  uint32_t encoded, decoded;

  START_TEST();

  for (encoded = 1; encoded < 256; encoded++) {
    decoded = rfc5444_timetlv_decode(encoded);
    CHECK_TRUE(decoded == _timetlv_table[encoded], "decode(%u) != %u", encoded, decoded);
  }

  END_TEST();
}

static void
test_timetlv_encoding_exact(void) {
  uint32_t encoded, decoded, i;

  START_TEST();

  for (i=1; i<256; i++) {
    if (_timetlv_table[i] == _timetlv_table[i-1]) {
      continue;
    }

    decoded = _timetlv_table[i];
    encoded = rfc5444_timetlv_encode(decoded);

    CHECK_TRUE(encoded == i, "encode(%u) != %u", decoded, i);
  }

  END_TEST();
}

static void
_do_timetlv_average_test(uint32_t decoded) {
  uint32_t encoded;

  encoded = rfc5444_timetlv_encode(decoded);

  CHECK_TRUE(_timetlv_table[encoded] > decoded, "encode(%u)=%u, decode(%u)=%u, %u <= %u",
      decoded, encoded,
      encoded, _timetlv_table[encoded],
      _timetlv_table[encoded-1], decoded);
  CHECK_TRUE(_timetlv_table[encoded-1] < decoded, "encode(%u)=%u, decode(%u-1)=%u, %u >= %u",
      decoded, encoded,
      encoded, _timetlv_table[encoded-1],
      _timetlv_table[encoded-1], decoded);
}

static void
test_timetlv_encoding_average(void) {
  uint32_t decoded;

  int i,j;


  START_TEST();

  for (i=1; i<256; i++) {
    if (_timetlv_table[i] - _timetlv_table[i-1] < 2) {
      continue;
    }

    for (j = 1; j<5; j++) {
      decoded = _timetlv_table[i] - j;

      if (_timetlv_table[i-1] < decoded) {
        _do_timetlv_average_test(decoded);
      }
    }
    for (j = 1; j<5; j++) {
      decoded = _timetlv_table[i-1] + j;

      if (_timetlv_table[i] > decoded) {
        _do_timetlv_average_test(decoded);
      }
    }
  }
  END_TEST();
}





